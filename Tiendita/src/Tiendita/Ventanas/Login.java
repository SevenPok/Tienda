    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        Usuario dato = new Usuario();
        Principal ventana = new Principal();
        if (jTextFieldUsuario.getText().length() > 0 && jTextFieldUsuario.getText().length() > 0) {
            if (jTextFieldUsuario.getText().equalsIgnoreCase("admin") && jPasswordField.getText().equals("admin")) {
                JOptionPane.showMessageDialog(null, "Bienvenido admin");
                ventana.setVisible(true);
                this.dispose();
            } else {
                dato.setUsuario(jTextFieldUsuario.getText());
                dato.setPassword(jPasswordField.getText());
                if (RegistroUsuario.getRegistroUsuario().login(dato)) {
                    JOptionPane.showMessageDialog(null, "Bienvenido " + dato.getUsuario());
                    UsuarioPrincipal window = new UsuarioPrincipal(UsuarioActual.getInstancia().getUsuario());
                    window.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Usuario o contrase√±a incorrecto");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese todos los campos");
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

