    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTextFieldNombre.getText().length() > 0 && jTextArea1.getText().length() > 0 && jTextFieldPrecio.getText().length() > 0
                && jTextFieldExistencia.getText().length() > 0 && jTextFieldImagen.getText().length() > 0) {

            producto.setNombre(jTextFieldNombre.getText());
            producto.setPrecio(Double.parseDouble(jTextFieldPrecio.getText()));
            producto.setDescripcion(jTextArea1.getText());
            producto.setDireccionImagen(jTextFieldImagen.getText());
            producto.setExistencia(Integer.parseInt(jTextFieldExistencia.getText()));
            RegistroProducto.getRegistroProducto().modify(producto);
            JOptionPane.showMessageDialog(null, "Se modifico correctamente");
            Clear_Table();
            RegistroProducto.getRegistroProducto().show(modelo);

        } else {
            JOptionPane.showMessageDialog(null, "Llene todos los campos");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void Clear_Table() {
        for (int i = 0; i < jTableProducto.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
        jTextArea1.setText("");
        jTextFieldExistencia.setText("");
        jTextFieldImagen.setText("");
        jTextFieldNombre.setText("");
        jTextFieldPrecio.setText("");
        jLabelImagen.setIcon(null);
    }

