    private void jTableProductoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProductoMouseClicked
        if (evt.getClickCount() == 2) {
            int fila = jTableProducto.getSelectedRow();
            try {
                producto = RegistroProducto.getRegistroProducto().search(fila);
                jTextFieldNombre.setText(producto.getNombre());
                jTextFieldPrecio.setText(Double.toString(producto.getPrecio()));
                jTextFieldImagen.setText(producto.getDireccionImagen());
                jTextArea1.setText(producto.getDescripcion());
                jTextFieldExistencia.setText(Integer.toString(producto.getExistencia()));
                ImageIcon imgIcon = new ImageIcon(jTextFieldImagen.getText());
                Icon icono = new ImageIcon(imgIcon.getImage().getScaledInstance(this.jLabelImagen.getWidth(), this.jLabelImagen.getHeight(), Image.SCALE_SMOOTH));
                jLabelImagen.setIcon(icono);
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jTableProductoMouseClicked

