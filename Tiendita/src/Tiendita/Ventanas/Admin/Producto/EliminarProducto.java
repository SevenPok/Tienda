    private void Clear_Table() {
        for (int i = 0; i < jTableProducto.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
        RegistroProducto.getRegistroProducto().show(modelo);
    }

    private void jTableProductoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProductoMouseClicked
        if (evt.getClickCount() == 2) {
            int fila = jTableProducto.getSelectedRow();
            try {
                producto = RegistroProducto.getRegistroProducto().search(fila);
                int salir = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro de eliminar " + producto.getNombre() + "?", "Alerta!", JOptionPane.YES_NO_OPTION);
                if (JOptionPane.YES_OPTION == salir) {
                    RegistroProducto.getRegistroProducto().delete(producto);
                    JOptionPane.showMessageDialog(null, "Se elimino correctamente");
                    Clear_Table();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jTableProductoMouseClicked

